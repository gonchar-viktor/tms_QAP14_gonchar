# 1. Дано целое число. Если оно является положительным, то прибавить к нему 1; в противном случае не изменять его. +
# Вывести полученное число.
def func2():
    global a
    a = -33
    if a >= 0:
        a += 1
    else:
        a = a
    return a


print(func2())


